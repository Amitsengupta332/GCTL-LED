function categoryButton(category) {
  const isActive = category === activeBlogCategory;

  return `
    <button
      type="button"
      data-blog-category="${category}"
      class="rounded-full border px-5 py-3 text-[12px] font-black transition ${
        isActive
          ? "border-[#0068d9] bg-[#0068d9] text-white shadow-[0_10px_24px_rgba(0,104,217,0.20)]"
          : "border-[#d7e4f2] bg-white text-[#40556d] hover:border-[#0068d9] hover:text-[#0068d9]"
      }"
    >
      ${category}
    </button>
  `;
}

function getFilteredBlogs() {
  if (activeBlogCategory === BLOGS_ALL_CATEGORY) return gctlBlogs;

  return gctlBlogs.filter((blog) => blog.category === activeBlogCategory);
}

function renderBlogCategories() {
  const categoriesWrapper = document.getElementById("blogsCategories");
  if (!categoriesWrapper) return;

  categoriesWrapper.innerHTML = getBlogCategories()
    .map(categoryButton)
    .join("");

  const buttons = categoriesWrapper.querySelectorAll("[data-blog-category]");

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      activeBlogCategory = button.getAttribute("data-blog-category");

      const url = new URL(window.location.href);

      if (activeBlogCategory === BLOGS_ALL_CATEGORY) {
        url.searchParams.delete("category");
      } else {
        url.searchParams.set("category", activeBlogCategory);
      }

      window.history.replaceState({}, "", url);

      renderBlogCategories();
      renderBlogsGrid();
    });
  });
}

function renderBlogsGrid() {
  const grid = document.getElementById("blogsGrid");
  const countText = document.getElementById("blogsCount");

  if (!grid || !countText) return;

  const filteredBlogs = getFilteredBlogs();

  grid.innerHTML = filteredBlogs.map(blogCard).join("");

  if (activeBlogCategory === BLOGS_ALL_CATEGORY) {
    countText.textContent = `Showing all ${gctlBlogs.length} blogs`;
  } else {
    countText.textContent = `Showing ${filteredBlogs.length} blog in ${activeBlogCategory}`;
  }
}

function initBlogsPage() {
  const grid = document.getElementById("blogsGrid");
  const countText = document.getElementById("blogsCount");

  if (!grid || !countText) return;

  const params = new URLSearchParams(window.location.search);
  const categoryFromUrl = params.get("category");

  if (categoryFromUrl && getOnlyBlogCategories().includes(categoryFromUrl)) {
    activeBlogCategory = categoryFromUrl;
  } else {
    activeBlogCategory = BLOGS_ALL_CATEGORY;
  }

  renderBlogCategories();
  renderBlogsGrid();
}

function waitForBlogsPage() {
  if (document.getElementById("blogsGrid")) {
    initBlogsPage();
    return;
  }

  const observer = new MutationObserver(() => {
    if (document.getElementById("blogsGrid")) {
      observer.disconnect();
      initBlogsPage();
    }
  });

  observer.observe(document.body, {
    childList: true,
    subtree: true,
  });
}

function getBlogSlugFromUrl() {
  const parts = window.location.pathname.split("/").filter(Boolean);
  const blogsIndex = parts.indexOf("blogs");

  if (blogsIndex === -1) return "";
  return parts[blogsIndex + 1] || "";
}

function findBlogBySlug(slug) {
  return gctlBlogs.find((blog) => blog.slug === slug);
}

function getRelatedBlogs(currentBlog) {
  const sameCategoryBlogs = gctlBlogs.filter(
    (blog) =>
      blog.slug !== currentBlog.slug && blog.category === currentBlog.category,
  );

  const otherBlogs = gctlBlogs.filter(
    (blog) =>
      blog.slug !== currentBlog.slug && blog.category !== currentBlog.category,
  );

  return [...sameCategoryBlogs, ...otherBlogs].slice(0, 3);
}

