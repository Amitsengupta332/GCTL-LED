function initProjectsPage() {
  const grid = document.getElementById("projectsGrid");
  const countText = document.getElementById("projectsCount");
  const filterButtons = document.querySelectorAll("[data-project-filter]");

  if (!grid || !countText || !filterButtons.length) return;

  let currentFilter = "all";

  function slugify(text) {
    return String(text)
      .toLowerCase()
      .replace(/&/g, "and")
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/(^-|-$)/g, "");
  }

  function getFilteredProjects() {
    if (currentFilter === "all") return gctlProjects;
    return gctlProjects.filter((project) => project.category === currentFilter);
  }

  function getBadgeClass(category) {
    const classes = {
      billboard: "bg-[#ff7a1a]",
      outdoor: "bg-[#0068d9]",
      indoor: "bg-[#0b8f6a]",
      corporate: "bg-[#0068d9]",
      government: "bg-[#1f5fbf]",
      education: "bg-[#008fbd]",
      healthcare: "bg-[#16a34a]",
      retail: "bg-[#f97316]",
    };

    return classes[category] || "bg-[#0068d9]";
  }

function projectCard(project) {
  const detailsLink = `/project-details.html?project=${slugify(project.title)}`;

  return `
    <article class="group overflow-hidden rounded-[14px] bg-white shadow-[0_12px_35px_rgba(7,31,77,0.08)] transition duration-300 hover:-translate-y-1 hover:shadow-[0_20px_55px_rgba(7,31,77,0.14)]">
      <a href="${detailsLink}" class="block overflow-hidden bg-[#eaf2fb]">
        <img
          src="${project.image}"
          alt="${project.title}"
          loading="lazy"
          class="h-[230px] w-full object-cover transition duration-500 group-hover:scale-105"
          onerror="this.src='/images/projects/project-placeholder.avif'"
        />
      </a>

      <div class="p-6">
        <span class="inline-flex rounded-[4px] ${getBadgeClass(project.category)} px-3 py-1 text-[11px] font-black text-white">
          ${project.categoryLabel}
        </span>

        <h3 class="mt-4 min-h-[60px] text-[20px] font-black leading-[1.25] tracking-[-0.4px] text-[#071f4d]">
          <a href="${detailsLink}" class="transition hover:text-[#0068d9]">
            ${project.title}
          </a>
        </h3>

        <div class="mt-3 flex items-center gap-2 text-[12px] font-bold text-[#697b90]">
          <span class="text-[#ff4d2d]">●</span>
          <span>${project.location}</span>
        </div>

        <p class="mt-4 min-h-[74px] text-[14px] font-semibold leading-7 text-[#52657d]">
          ${project.description}
        </p>

        <a
          href="${detailsLink}"
          class="mt-5 inline-flex items-center gap-2 text-[13px] font-black text-[#0068d9] transition hover:gap-3"
        >
          View Details
          <span>→</span>
        </a>
      </div>
    </article>
  `;
}

  function setActiveFilter(activeButton) {
    filterButtons.forEach((button) => {
      button.classList.remove("bg-[#0068d9]", "text-white");
      button.classList.add("bg-white", "text-[#24374f]");
    });

    activeButton.classList.remove("bg-white", "text-[#24374f]");
    activeButton.classList.add("bg-[#0068d9]", "text-white");
  }

  function renderProjects() {
    const projects = getFilteredProjects();

    grid.innerHTML = projects.map(projectCard).join("");

    countText.textContent =
      currentFilter === "all"
        ? `Showing all ${projects.length} projects`
        : `Showing ${projects.length} ${currentFilter} projects`;
  }

  filterButtons.forEach((button) => {
    button.addEventListener("click", () => {
      currentFilter = button.dataset.projectFilter;
      setActiveFilter(button);
      renderProjects();
    });
  });

  renderProjects();
}

function waitForProjectsPage() {
  if (document.getElementById("projectsGrid")) {
    initProjectsPage();
    return;
  }

  const observer = new MutationObserver(() => {
    if (document.getElementById("projectsGrid")) {
      observer.disconnect();
      initProjectsPage();
    }
  });

  observer.observe(document.body, {
    childList: true,
    subtree: true,
  });
}

waitForProjectsPage();